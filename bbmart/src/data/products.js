// Get products by category
export const getProductsByCategory = (category) => {
  if (!category) return products;
  return products.filter(product => product.category === category);
};

// Get product by ID
export const getProductById = (id) => {
  return products.find(product => product.id === parseInt(id));
};

// Get related products
export const getRelatedProducts = (productId, limit = 4) => {
  const product = getProductById(productId);
  if (!product) return products.slice(0, limit);
  
  // Get products from the same category, excluding the current product
  let related = products.filter(p => p.category === product.category && p.id !== product.id);
  
  // If we don't have enough related products, add some from other categories
  if (related.length < limit) {
    const otherProducts = products.filter(p => p.category !== product.category && p.id !== product.id);
    related = [...related, ...otherProducts].slice(0, limit);
  } else {
    related = related.slice(0, limit);
  }
  
  return related;
};

// Get popular products
export const getPopularProducts = (limit = 8) => {
  return [...products]
    .sort((a, b) => b.reviewCount - a.reviewCount)
    .slice(0, limit);
};

// Get discounted products
export const getDiscountedProducts = (limit = 8) => {
  return products
    .filter(product => product.discount)
    .sort((a, b) => b.discount - a.discount)
    .slice(0, limit);
};
